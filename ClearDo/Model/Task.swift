import Foundation

class Task {

    var text: String?
    var isDone: Bool
    var createdAt: Date
    var updatedAt: Date

    init(text: String?, isDone: Bool = false, createdAt: Date, updatedAt: Date) {
        self.isDone = isDone
        self.text = text
        self.createdAt = createdAt
        self.updatedAt = updatedAt
    }

    static func empty() -> Task {
        Task(text: nil, createdAt: Date(), updatedAt: Date())
    }

}
